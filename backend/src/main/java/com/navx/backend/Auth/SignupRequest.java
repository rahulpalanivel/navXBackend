package com.navx.backend.Auth;


import java.util.Set;

import com.navx.backend.model.RoleTypes;
import jakarta.validation.constraints.*;

public class SignupRequest {
    @NotBlank
    @Size(min = 3, max = 20)
    private String username;

    @NotBlank
    @Size(max = 50)
    @Email
    private String email;

    @NotBlank
    @Size(min = 6, max = 40)
    private String password;

    @NotBlank
    @Size(max = 50)
    private String phoneno;

    @NotBlank
    @Size(max = 50)
    private String address;

    private String gender;

    private Set<RoleTypes> roles;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getPhoneno() {
        return phoneno;
    }

    public void setPhoneno(String phoneno) {
        this.phoneno = phoneno;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public void setRoles(Set<RoleTypes> roles) {
        this.roles = roles;
    }

    public Set<RoleTypes> getRoles() {
        return this.roles;
    }

    public void setRole(Set<RoleTypes> roles) {
        this.roles = roles;
    }


}
